class Car:
    def __init__(self, color, model, year):
        self.color = color
        self.model = model
        self.year = year

def drive (self):
    print(f"The{self.color} {self.model} {self.year} is driving.")

def stop (self):
    print(f"The {self.color} {self.model} {self.year} has stopped.")   
